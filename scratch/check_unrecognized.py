levels_dir = r"f:\myprog\menasa\rowaq-app\levels"
seniors_dir = os.path.join(levels_dir, "Seniors")

pdf_files = []
for f in range(2, 11):
    pdf_files.append(os.path.join(levels_dir, f"children {f}.pdf"))
for f in os.listdir(seniors_dir):
    if f.endswith(".pdf"):
        pdf_files.append(os.path.join(seniors_dir, f))

unrecognized = set()

for path in pdf_files:
    if not os.path.exists(path):
        continue
    with pdfplumber.open(path) as pdf:
        for page in pdf.pages:
            tables = page.extract_tables()
            for table in tables:
                for row in table:
                    if not row or len(row) < 22:
                        continue
                    
                    # Columns that contain Surah names: 4, 6, 8, 10, 12, 14, 16, 18
                    for idx in [4, 6, 8, 10, 12, 14, 16, 18]:
                        val = row[idx]
                        if val:
                            val = str(val).strip()
                            if val in ["*", "**", "", None]:
                                continue
                            if val in surah_mapping:
                                continue
                            rev = val[::-1]
                            if rev not in surah_names:
                                unrecognized.add((val, rev, os.path.basename(path)))

print("Unrecognized Surah names:")
for val, rev, filename in sorted(unrecognized):
    print(f"  Raw: {val} | Reversed: {rev} | File: {filename}")
