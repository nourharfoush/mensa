surah_set = set(surah_names)

def get_surah_name(s):
    if not s:
        return ""
    
    # Remove newlines and strip
    s = s.replace('\n', '').replace('\r', '')
    s = re.sub(r'\s+', ' ', s).strip()
    
    if s in ["*", "**", "", None]:
        return ""
        
    s = unicodedata.normalize('NFKC', s)
    
    if s in surah_set:
        return s
    if s in surah_mapping:
        return surah_mapping[s]
        
    # Check reverse
    rev = s[::-1]
    if rev in surah_set:
        return rev
    if rev in surah_mapping:
        return surah_mapping[rev]
        
    return rev

# Now let's test this logic against all invalid names we collected!
with open("scratch/invalid_surahs_list.txt", "r", encoding="utf-8") as f:
    lines = f.readlines()

resolved = 0
failed = 0

for line in lines:
    line = line.strip()
    if not line:
        continue
    # Line format: 'surah': count
    match = re.match(r"^'(.+?)':\s*(\d+)$", line)
    if match:
        name = match.group(1)
        count = int(match.group(2))
        res = get_surah_name(name)
        if res in surah_set:
            resolved += count
        else:
            failed += count
            print(f"FAILED to resolve: '{name}' -> '{res}'")

print(f"Total occurrences: {resolved + failed}")
print(f"Resolved: {resolved}")
print(f"Failed: {failed}")
